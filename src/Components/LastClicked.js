class LastClicked {
    constructor() {
      this.col=null;
      this.row=null;
    }
  
    setCol(value) {
      this.col=value;
    }
    setRow(value) {
        this.row=value;
      }
      getCol() {
        return this.col;
      }
      getRow() {
        return this.row;
      }
    
  }
  export default LastClicked;
  